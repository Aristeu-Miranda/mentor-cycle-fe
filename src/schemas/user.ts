import { availabilitySchema } from "schemas/availability";
import { z } from "zod";

export const userSchema = z.object({
  id: z.string(),
  email: z.string().email(),
  password: z.string().nullable(),
  active: z.boolean().nullable(),
  firstName: z.string(),
  lastName: z.string().nullable(),
  photoUrl: z.string().url(),
  yearsOfExperience: z.number().nullable(),
  isEmailVerified: z.boolean(),
  isTermsAccepted: z.boolean(),
  onBoardingCompleted: z.boolean(),
  createdAt: z.date(),
  updatedAt: z.date(),
  googleId: z.string().nullable(),
  facebookId: z.string().nullable(),
  birthDate: z.date(),
  country: z.string(),
  state: z.string(),
  city: z.string(),
  skills: z.array(z.string()).nullable(),
  linkedin: z.string().url().nullable(),
  github: z.string().url().nullable(),
  website: z.string().url().nullable(),
  jobTitle: z.string().url().nullable(),
  jobCompany: z.string().url().nullable(),
  biography: z.string().url().nullable(),
  description: z.string().url(),
  isMentor: z.boolean().nullable(),
  status: z.string().nullable(),
  availability: z.array(availabilitySchema).nullable(),
});
